using UnityEngine;

/************************************************************************************************************/
/********************************************~][FirstStarting_***********************************************/
/************************************************************************************************************/
/*******************************************[Первый запуск игры]*********************************************/
/*****************************************1)Настройка PlayerPrefab*******************************************/
/************************************************************************************************************/

public class MBFirstStarting : MonoBehaviour
{
    public void Awake()
    {
        if (!PlayerPrefs.HasKey(GameSettingData.CHECK_FIRST_STARTING))
        {
            PlayerPrefs.SetInt(GameSettingData.CHECK_FIRST_STARTING, 1);
        }
    }
}
